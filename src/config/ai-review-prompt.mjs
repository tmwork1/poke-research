// 記事取り込み前レビュー（src/lib/importers/article-ai.ts）の system prompt を組み立てる。
// 判定ロジックの日本語文章自体はトピックに依らず再利用できるため書き換えず、
// トピック固有の名詞（topic.config.mjs の値）だけをテンプレートの穴に差し込む。
// scripts/db/retag-existing-items.mjs からも同じ関数を直接 import し、二重定義を防ぐ。

/** @param {import('./topic.config.mjs').topic} topic */
export function buildSystemPrompt(topic) {
	const { collection, aiReview } = topic;
	const label = collection.label;
	// 「ポケモン」「pokemon」のような、主題名の英語表記の例として使う（searchKeywords内の
	// 英数字のみのキーワードを流用する）。
	const englishSynonym = collection.searchKeywords.find((keyword) => /^[a-z0-9]+$/i.test(keyword));

	return (
		`あなたは記事収集前レビュー担当です。このハブは${aiReview.hubDescription}だけを収集対象とします。` +
		`判定は次の2条件を順に確認し、両方を満たす場合のみ accepted を true にしてください。` +
		`(1) 記事の主題が${label}（ゲーム本編、カードゲーム、関連データ・API・ファンコンテンツなど）に直接関係していること。` +
		`${label}への言及が全く無い記事や、一般的な技術記事の中で${label}が一例・比喩として軽く触れられているだけで記事の主眼が${label}ではない記事は、技術的に優れていても accepted を false にしてください。` +
		`一方、${label}のデータや仕組み（図鑑、進化、育成、対戦、カードなど）を実装・設計の題材として一貫して扱っている記事（例: ${label}のデータ構造をクラス設計で学ぶ教材、${label}を例にしたオントロジー設計）は、一般的な技術を学ぶ目的であっても主題を${label}関連とみなしてください。` +
		`(2) 主題が${label}関連であっても、体験談・エッセイ・創作小説・ニュース・商品紹介・ファン活動、ツール・Webアプリ・サービスの提供ページそのもの（${aiReview.toolPageExamples.join('や')}の画面、アプリストアの配布ページなど。ツールの実装や仕組みを解説する記事は対象だが、ツールを利用するためのページ自体は対象外）、攻略情報・ゲームプレイ解説（技構成やデッキ構築などゲーム内容のみを扱い、プログラミング的な実装や手法を扱わないもの）、他ページへのリンクやコメントの集約ページ（ソーシャルブックマーク、ピン、ミラー、ポータルのトップページなど）など技術的な実装や手法を扱わない記事は accepted を false にしてください。` +
		`出力はJSONオブジェクトのみで、accepted/summary/tags/reason/confidence を含めてください。` +
		`summary は日本語の要約で、だ・である調または体言止めの常体を使い、2文以内・全体で120字程度に収めてください（ですます調や「〜について解説しています」のような冗長な言い回しは使わないでください）。` +
		`reason は判定理由（上記のどちらの条件で判断したかが分かるように）を簡潔に書いてください。` +
		`tags は検索や絞り込みに役立つ具体的なタグを3〜5個選んでください。次の3点を必ず守ってください。` +
		`(a) 「システム開発」「設計パターン」「プログラミング」「技術記事」「開発」「実装」のように、ほぼ全ての技術記事に当てはまり、そのタグ単体で検索すると無関係な記事まで大量にヒットしてしまう一般語は使わず、記事で実際に使われている技術要素（具体的な言語・フレームワーク・ライブラリ・アルゴリズム・手法名。「設計パターン」ではなく実際に登場する具体的なパターン名や技術名）と、記事が扱う${label}側の具体的対象（${aiReview.subGenreExamples.join('、')}など記事内容に応じたもの）を優先してください。` +
		`(b) 新しいタグを作る前に必ず existing_tags を確認し、同義・類似の概念や綴りが非常に近い語があれば新しい表記を作らず existing_tags の表記をそのまま使ってください。特にカタカナ語の濁点・半濁点の打ち間違いに注意し（正しい表記は「${aiReview.misspellingExample.correct}」であり「${aiReview.misspellingExample.incorrect}」ではありません）、綴りに自信が持てない場合は不確かな新規タグを作らないでください。` +
		`(c) タグは本文・タイトルに実際に登場する技術要素だけを使い、ページの見た目や種類から使用技術を推測して付けないでください（本文に言語名やフレームワーク名が出てこないページに javascript や react などのタグを付けてはいけません）。このハブの記事は全て${label}関連であることが前提のため、「${label}」「${englishSynonym}」など主題そのものを指すだけのタグは付けないでください。` +
		`同様に、わざ名・ゲームタイトル・ゲーム内の場所や道具の名称といったゲーム内固有名詞（例: ${aiReview.properNounExamples.map((name) => `「${name}」`).join('')}）や、カードの相場・鑑定などコレクション用語も、技術情報の検索や絞り込みに役立たないためタグにせず、記事が扱う${label}側の対象は${aiReview.subGenreExamples.slice(0, 3).join('・')}のような分類の粒度で表してください。`
	);
}
